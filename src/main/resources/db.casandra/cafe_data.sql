SELECT
  cafe_tables.id,
  cafe_tables.waiter_id,
  bill_dates.bill_date
    + interval '1 hour' * abs(18 + random() * 2)
    + interval '1 minute' * abs(60 + random() * 2)
    + interval '1 second' * abs(60 + random() * 2),
  abs(2 + (random() * 3)),
  0
FROM bill_dates, cafe_tables;

